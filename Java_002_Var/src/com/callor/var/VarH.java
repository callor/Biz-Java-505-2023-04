package com.callor.var;

public class VarH {
	public static void main(String[] args) {
		
		int num = 0 ;
		num = num + 1;
		num += 1;
		num ++; // num 변수에 저장 값을 1증가
		
		num = num - 1;
		num -= 1;
		num --; // num 변수에 저장 값을 1감소
		
		
		num++;
		num++;
		num++;
		num++;
		num++;
		
		
		
		
		
	}

}
